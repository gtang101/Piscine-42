#include <unistd.h>

void	ft_print_comb(void)
{
	int	j;
	int	k;
	int	l;

	j = '0';
	while (j <= '7')
	{
		k = j;
		while (++k <= '8')
		{
			l = k + 1;
			while (l <= '9')
			{
				write (1, &j, 1);
				write (1, &k, 1);
				write (1, &l, 1);
				if (j != '7' || k != '8' || l != '9')
				{
					write (1, ", ", 2);
				}
				l++;
			}
		}
		j++;
	}
}

int	main(void)
{
	ft_print_comb();
}

